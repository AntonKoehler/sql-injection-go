# Тестовый запрос к уязвимому endpoint, возвращающему список студентов
# Пример запроса, который просто возвращает всех студентов (условие "1=1")
curl "http://localhost:8080/students?query=1=1"

# Тестовый запрос, демонстрирующий возможность SQL-инъекции.
# В этом примере можно попробовать ввести заведомо истинное условие или даже попытку модификации запроса.
curl "http://localhost:8080/students?query=1=1%20OR%20'1'='1'"

# Тестовый запрос к безопасному endpoint, использующему параметризованные запросы (без SQL-инъекции)
curl "http://localhost:8080/students_safe"